package com.cyt.smarthome.bean;

public class PeripheralService {
    private String surrounding_name;
    private String surrounding_content;
    private String surrounding_addr;
    private String surrounding_phone;
    private String surrounding_type;

    public String getSurrounding_type() {
        return surrounding_type;
    }

    public void setSurrounding_type(String surrounding_type) {
        this.surrounding_type = surrounding_type;
    }

    public String getSurrounding_name() {
        return surrounding_name;
    }

    public void setSurrounding_name(String surrounding_name) {
        this.surrounding_name = surrounding_name;
    }

    public String getSurrounding_content() {
        return surrounding_content;
    }

    public void setSurrounding_content(String surrounding_content) {
        this.surrounding_content = surrounding_content;
    }

    public String getSurrounding_addr() {
        return surrounding_addr;
    }

    public void setSurrounding_addr(String surrounding_addr) {
        this.surrounding_addr = surrounding_addr;
    }

    public String getSurrounding_phone() {
        return surrounding_phone;
    }

    public void setSurrounding_phone(String surrounding_phone) {
        this.surrounding_phone = surrounding_phone;
    }
}
